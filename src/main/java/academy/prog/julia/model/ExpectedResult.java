package academy.prog.julia.model;

/**
 * Enum representing the possible types of expected results.
 *
 * This enum is used to define and categorize the expected outcomes of certain operations.
 */
public enum ExpectedResult {
    /**
     * Indicates that the expected result is a link.
     */
    LINK,

    /**
     * Indicates that the expected result is a file.
     */
    FILE
}
